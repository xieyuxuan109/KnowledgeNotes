package main

import "fmt"

func main() {
	// 	Go 语言运算符
	// 一、算术运算符
	// 运算符	名称	示例	说明
	// +	加法	a + b	数值相加或字符串连接
	// -	减法	a - b	数值相减
	// *	乘法	a * b	数值相乘
	// /	除法	a / b	数值相除（整数除法会截断小数）
	fmt.Println(10 / 4)   //如果参与运算的数都是整数，结果为整数 类似C语言
	fmt.Println(10.0 / 4) //2.5
	// %	取模	a % b	求余数（只适用于整数） a%b=a-a/b*b
	fmt.Println(-10 % 3) //-1
	fmt.Println(10 % 3)  //1
	// ++	自增	a++	变量值加1（不能用于表达式）自能作为独立语句
	var a int = 1
	a++ //2
	fmt.Println(a)
	// --	自减	a--	变量值减1（不能用于表达式）自能作为独立语句
	a-- //1
	fmt.Println(a)
	// 注意：Go 中没有 ++a 或 --a 的前缀形式。

	// 二、关系（比较）运算符
	// 运算符	名称	示例	说明
	// ==	等于	a == b	判断值是否相等
	// !=	不等于	a != b	判断值是否不相等
	// >	大于	a > b	判断左值是否大于右值
	// <	小于	a < b	判断左值是否小于右值
	// >=	大于等于	a >= b	判断左值是否大于等于右值
	// <=	小于等于	a <= b	判断左值是否小于等于右值

	// 三、逻辑运算符
	// 运算符	名称	示例	说明
	// &&	逻辑与	a && b	两个条件都为 true 时结果为 true
	// ||	逻辑或	a || b	至少一个条件为 true 时结果为 true
	// !	逻辑非	!a	条件取反

	// 四、位运算符
	// 运算符	名称	示例	说明
	// &	按位与	a & b	对应位都为1时结果为1
	// |	按位或	a | b	对应位至少一个为1时结果为1
	// ^	按位异或	a ^ b	对应位不同时结果为1
	// &^	按位清除	a &^ b	将b中为1的位在a中清零
	// <<	左移	a << n	左移n位，低位补0
	// >>	右移	a >> n	右移n位，高位补符号位

	// 五、赋值运算符
	// 运算符	名称	示例	等价于
	// =	赋值	a = b	-
	// +=	加后赋值	a += b	a = a + b
	// -=	减后赋值	a -= b	a = a - b
	// *=	乘后赋值	a *= b	a = a * b
	// /=	除后赋值	a /= b	a = a / b
	// %=	取模后赋值	a %= b	a = a % b
	// &=	按位与后赋值	a &= b	a = a & b
	// |=	按位或后赋值	a |= b	a = a | b
	// ^=	按位异或后赋值	a ^= b	a = a ^ b
	// &^=	按位清除后赋值	a &^= b	a = a &^ b
	// <<=	左移后赋值	a <<= n	a = a << n
	// >>=	右移后赋值	a >>= n	a = a >> n

	// 六、其他运算符
	// 运算符	名称	示例	说明
	// &	取地址	&a	获取变量地址
	// *	解引用	*a	获取指针指向的值
	// <-	通道操作	ch <- x	发送x到通道ch
	// <-	通道操作	x = <-ch	从通道ch接收值到x
	// :	切片/标签	a[1:4]	创建切片或标签声明
	// .	选择器	obj.field	访问结构体字段或包成员
	// ...	可变参数	func(a ...int)	表示可变参数

	// 七、运算符优先级（从高到低）
	// 优先级	运算符	说明
	// 1	* / % << >> & &^	乘法、除法、取模、移位
	// 2	+ - | ^	加法、减法、位运算
	// 3	== != < <= > >=	比较运算符
	// 4	&&	逻辑与
	// 5	||	逻辑或
}
